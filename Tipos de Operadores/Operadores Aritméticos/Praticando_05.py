#Declarando variáveis:
produto_1 = 10
produto_2 = 20

#Imprimindo as informações:
print(produto_1 + produto_2) #somando
print(produto_1 - produto_2) #subtraindo
print(produto_1 // produto_2) #divisão decimal
print(produto_1 / produto_2) #divisão inteira
print(produto_1 * produto_2) #multiplicação
print(produto_1 % produto_2) #resto da divisão
print(produto_1 ** produto_2) #exponenciação


# O Python entende a ordem dos fatores (Imporcedência numérica):
x = 10 + 5 * 4
print(x)

